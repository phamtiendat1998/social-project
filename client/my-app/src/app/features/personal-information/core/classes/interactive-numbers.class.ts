export class InteractiveNumbers {
    ImageQuantity: number;
    FriendQuantity: number;
    FollowQuantity: number;
    VideoQuantity: number;
    constructor(
        imageQuantity: number,
        friendQuantity: number,
        followQuantity: number,
        videoQuantity: number,
    ) {
        this.ImageQuantity = imageQuantity;
        this.FriendQuantity = friendQuantity;
        this.FollowQuantity = followQuantity;
        this.VideoQuantity = videoQuantity;
    }
}
